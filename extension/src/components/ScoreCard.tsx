// ── Animated score ring ───────────────────────────────────────────────────────

function ScoreRing({ score }: { score: number }) {
  const size = 92
  const r    = size * 0.38
  const circ = 2 * Math.PI * r
  const sw   = size * 0.085
  const [offset, setOffset]   = useState(circ)
  const [display, setDisplay] = useState(0)

  useEffect(() => {
    const t0 = performance.now(), dur = 1400
    let raf: number
    const tick = (now: number) => {
      const p = Math.min(1, (now - t0) / dur)
      const e = 1 - Math.pow(1 - p, 3)
      setDisplay(Math.round(score * e))
      setOffset(circ * (1 - (score / 100) * e))
      if (p < 1) raf = requestAnimationFrame(tick)
    }
    raf = requestAnimationFrame(tick)
    return () => cancelAnimationFrame(raf)
  }, [score])

  return (
    <div style={{ position: "relative", width: size, height: size, flexShrink: 0 }}>
      <svg width={size} height={size} viewBox={`0 0 ${size} ${size}`} style={{ transform: "rotate(-90deg)" }}>
        <circle cx={size/2} cy={size/2} r={r} fill="none" stroke="rgba(0,0,0,0.1)" strokeWidth={sw}/>
        <circle cx={size/2} cy={size/2} r={r} fill="none" stroke="rgba(0,0,0,0.75)" strokeWidth={sw}
          strokeDasharray={circ} strokeDashoffset={offset} strokeLinecap="round"
          style={{ transition: "stroke-dashoffset 0.05s" }}
        />
      </svg>
      <div style={{ position: "absolute", inset: 0, display: "flex", flexDirection: "column", alignItems: "center", justifyContent: "center" }}>
        <span style={{ fontSize: 28, fontWeight: 900, color: "rgba(0,0,0,0.85)", lineHeight: 1, letterSpacing: "-1.5px" }}>{display}</span>
        <span style={{ fontSize: 11, color: "rgba(0,0,0,0.5)", fontWeight: 600 }}>/ 100</span>
      </div>
    </div>
  )
}

