// ── Verdict helpers ───────────────────────────────────────────────────────────

function verdictData(s: number) {
  if (s >= 75) return { label: "BUY",   text: "Buy with confidence",  sub: "Strong sustainability record",  accent: "#16A34A", heroFrom: "#D1FAE5", heroTo: "#ECFDF5" }
  if (s >= 60) return { label: "BUY",   text: "Good eco choice",      sub: "Above-average sustainability",  accent: "#2563EB", heroFrom: "#DBEAFE", heroTo: "#EFF6FF" }
  if (s >= 45) return { label: "PAUSE", text: "Consider alternatives", sub: "Some environmental concerns",  accent: "#D97706", heroFrom: "#FEF3C7", heroTo: "#FFFBEB" }
  return             { label: "SKIP",  text: "Skip if possible",      sub: "High environmental impact",    accent: "#DC2626", heroFrom: "#FEE2E2", heroTo: "#FFF5F5" }
}

function barColor(s: number) {
  if (s >= 65) return "#16A34A"
  if (s >= 40) return "#D97706"
  return "#DC2626"
}

function estimateCO2(score: number) {
  const kg = +(2 + (100 - score) * 0.12).toFixed(1)
  return { kg, km: Math.round(kg / 0.12) }
}

function confidenceDot(c: string) {
  if (c === "High")   return "#16A34A"
  if (c === "Medium") return "#D97706"
  return "#9CA3AF"
}

